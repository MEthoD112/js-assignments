/**
 * Returns the string representation of rectangle with specified width and height
 * using pseudograhic chars
 *
 * @param {number} width
 * @param {number} height
 * @return {string}
 *
 * @example
 *
 *            '┌────┐\n'+
 *  (6,4) =>  '│    │\n'+
 *            '│    │\n'+
 *            '└────┘\n'
 *
 *  (2,2) =>  '┌┐\n'+
 *            '└┘\n'
 *
 *             '┌──────────┐\n'+
 *  (12,3) =>  '│          │\n'+
 *             '└──────────┘\n'
 *
 */
function getRectangleString(width, height) {
    let topStr = '';
    let middleStr = '';
    let bottomStr = '';
    let middleStrings = '';
    let str = '';
    let str1 = '';
    for (let i = 0; i < width-2; i++){
        str += '─';
        str1 += ' ';
    }
    topStr = '┌' + str + '┐'+ '\n';
  
    middleStr = '│'+ str1 + '│'  + '\n';

    bottomStr = '└' + str + '┘'+ '\n';


    for (let i = 3; i <= height; i++){
        middleStrings += middleStr;
    }
  
    return topStr+middleStrings+bottomStr;
}


/**
 * Encode specified string with ROT13 cipher
 * See details:  https://en.wikipedia.org/wiki/ROT13
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *
 *   'hello' => 'uryyb'
 *   'Why did the chicken cross the road?' => 'Jul qvq gur puvpxra pebff gur ebnq?'
 *   'Gb trg gb gur bgure fvqr!' => 'To get to the other side!'
 *   'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz' => 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm'
 *
 */
function encodeToRot13(str) {
    let upper = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    let lower = 'abcdefghijklmnopqrstuvwxyz';
    
    let newstr = '';
    for (let i = 0; i < str.length; i++){
        if (str.charAt(i) === ' '||str.charAt(i)=== '?'||str.charAt(i)==='!'){
            newstr += str.charAt(i);
            continue;
        }
        if (str.charAt(i) === str.charAt(i).toUpperCase()){
            let index = upper.indexOf(str.charAt(i));
            if (index > 12){
                index = index + 13 - 26;
            }else{
                index +=13;
            }
            newstr += upper.charAt(index);
        }
        if (str.charAt(i) === str.charAt(i).toLowerCase()){
            let index = lower.indexOf(str.charAt(i));
            if (index > 12){
                index = index + 13 - 26;
            }else{
                index +=13;
            }
            newstr += lower.charAt(index);
        }
    }
    return newstr;
}

/**
 * Returns true if the value is string; otherwise false.
 * @param {string} value
 * @return {boolean}
 *
 * @example
 *   isString() => false
 *   isString(null) => false
 *   isString([]) => false
 *   isString({}) => false
 *   isString('test') => true
 *   isString(new String('test')) => true
 */
function isString(value) {
    if ((typeof value) === 'string' || value instanceof String){
        return true;
    }
    return false;
}


